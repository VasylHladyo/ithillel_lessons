console.log('number' + 23 + 32);
/*
 В даному прикладі, через те що внас є рядок і знак плюс при операціях з рядком це операція конкатинації то усі
 значення приводяться до типу рядок. Тому результатом даної команди буде: 'number2332'
*/

console.log(41 + 1 + 'number');
/*
 Цей приклад частково похожий до першого, але з невеликими відмінностями.
 Код в JS виконується послідовно, тому спочатку зробиться арифметична операція додавання(41 + 1 = 42).
 Далі, через те що внас іде рядок, то відбудеться операція конкатинації. Число 42 - перетвориться в рядок.
 Тому результатом даної команди буде: '42number'
*/

console.log(null + 1);
/*
 В даному прикладі арифметична операція.
 "null" - перетвориться в число і стане 0
 Тому результатом даної команди буде: 0 + 1 = 1.
*/

console.log('five' + + 'two');
/*
 Не до кінця впевнений в своїх висновках, але спочатку в даному випадку буде спроба перетворити "two" в число, через те
 що перед цим значенням стоїть "+" то буде спроба перетворити рядок в число. Через те що в нас рядок без чисел, то
 результатом перетворення "two" буде "NaN". Далі буде відбуватися процес конкатиинації "five" + "NaN".
 Тому результатом даної команди буде "fiveNaN"
*/

console.log(2 && 7);
/*
В даному випадку відбувається логічна операція "&&". Даний логічний оператор повертає значення "true", лише тоді,
коли два операнди маються значення "true". В усіх інших випадках повернеться "fasle".
Через те, що "2" - "true" та "7" - "true" нам повернеться останнє значення "true". А це "7".
*/

console.log(+'40' + +'2');
/*
1) Рядок "40" перетворюється в число 40
2) Рядок "2" перетворюється в число 2
3) 40 + 2 = 42
*/

console.log('10' - 5 === 6);
/*
1) Перетворення рядка "10" в число = 10
2) Арифметична операція віднімання 10 - 5 = 5
3) Логічна операція строгого порівняння 5 === 6
4) Результатом виконання даної операції буде "false"
*/

console.log(true + false);
